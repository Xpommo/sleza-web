function checkSection(id, text) {
  const t = text.toLowerCase();
  const c = PROPOSED[id];
  const hasSection = sectionHeadingHits[id] ? sectionHeadingHits[id].test(text) : false;
  const hasPattern = c.patterns.some(p => p.test(t));
  const hasKeyword = c.keywords.some(k => t.includes(k.toLowerCase()));
  return { present: hasSection || hasPattern || hasKeyword, hasSection, hasPattern, hasKeyword };
}

// ── Test cases ────────────────────────────────────────────────────────────────

let passed = 0, failed = 0;

function test(label, id, text, expectPresent) {
  const r = checkSection(id, text);
  const ok = r.present === expectPresent;
  if (ok) {
    passed++;
    console.log(`  ✅ ${label}`);
  } else {
    failed++;
    const why = `hasSection=${r.hasSection} hasPattern=${r.hasPattern} hasKeyword=${r.hasKeyword}`;
    console.log(`  ❌ ${label}  [expected=${expectPresent}, ${why}]`);
  }
}

